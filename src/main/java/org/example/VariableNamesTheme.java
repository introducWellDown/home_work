package org.example;

public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("\n1. Разные переменные");
        // Цифра
        int digit = 1;
        System.out.println("Цифра: " + digit);

        // Сумма чисел
        int digitSum = 1 + 2 + 3 + 4 + 5;
        System.out.println("Сумма чисел: " + digitSum);

        // Произведение цифр
        int digitProduct = 1 * 2 * 3 * 4 * 5;
        System.out.println("Произведение цифр: " + digitProduct);

        // Максимальное число
        int maxNumber = 100;
        System.out.println("Максимальное число: " + maxNumber);

        // Количество десятков
        int tensCount = 5;
        System.out.println("Количество десятков: " + tensCount);

        // Вес собаки
        double dogWeight = 12.5;
        System.out.println("Вес собаки: " + dogWeight);

        // Исходное число
        int initialNumber = 12345;
        System.out.println("Исходное число: " + initialNumber);

        // Процент по вкладу
        double depositRate = 5.5;
        System.out.println("Процент по вкладу: " + depositRate);

        // Переменная хранит символ &
        char ampersand = '&';
        System.out.println("Переменная хранит символ: " + ampersand);

        // Код ошибки
        int errorCode = 404;
        System.out.println("Код ошибки: " + errorCode);

        // Тип сообщения
        String messageType = "Warning";
        System.out.println("Тип сообщения: " + messageType);

        // Число нулей
        int zeroCount = 3;
        System.out.println("Число нулей: " + zeroCount);

        // Уникальное число
        int uniqueNumber = 123456789;
        System.out.println("Уникальное число: " + uniqueNumber);

        // Случайное число
        double randomNumber = Math.random();
        System.out.println("Случайное число: " + randomNumber);

        // Математическое выражение
        String mathExpression = "3 + 4 * 5";
        System.out.println("Математическое выражение: " + mathExpression);

        // Счет в игре
        int gameScore = 2500;
        System.out.println("Счет в игре: " + gameScore);

        // Максимальная длина
        int maxLength = 255;
        System.out.println("Максимальная длина: " + maxLength);

        // Пункт меню
        String menuOption = "File";
        System.out.println("Пункт меню: " + menuOption);

        // Стоимость кофе
        double coffeePrice = 3.75;
        System.out.println("Стоимость кофе: " + coffeePrice);

        // Дата начала
        String startDate = "2024-11-06";
        System.out.println("Дата начала: " + startDate);

        // Окончание диапазона
        int rangeEnd = 100;
        System.out.println("Окончание диапазона: " + rangeEnd);

        // Полное имя работника месяца
        String employeeOfTheMonth = "Иванов Иван Иванович";
        System.out.println("Полное имя работника месяца: " + employeeOfTheMonth);

        // Заголовок электронной книги
        String eBookTitle = "Effective Java";
        System.out.println("Заголовок электронной книги: " + eBookTitle);

        // Размер
        int size = 1024;
        System.out.println("Размер: " + size);

        // Вместимость
        int capacity = 50;
        System.out.println("Вместимость: " + capacity);

        // Счетчик
        int counter = 0;
        System.out.println("Счетчик: " + counter);

        // Путь до файла
        String filePath = "C:/Users/user/documents/file.txt";
        System.out.println("Путь до файла: " + filePath);

        // Количество чисел в каждой строке
        int numbersPerLine = 10;
        System.out.println("Количество чисел в каждой строке: " + numbersPerLine);

        System.out.println("\n2. Boolean-переменные");
        // Boolean-переменные
        boolean isEqualHundreds = true;
        System.out.println("Сотни равны?: " + isEqualHundreds);

        boolean isComputerOn = false;
        System.out.println("Компьютер включен?: " + isComputerOn);

        boolean hasEqualDigits = true;
        System.out.println("Есть равные цифры?: " + hasEqualDigits);

        boolean isCreated = true;
        System.out.println("Создано?: " + isCreated);

        boolean isEmpty = false;
        System.out.println("Пустое?: " + isEmpty);

        boolean isActive = true;
        System.out.println("Активное?: " + isActive);

        boolean isNew = true;
        System.out.println("Новое?: " + isNew);

        boolean isEmailValid = true;
        System.out.println("Электронная почта действительная?: " + isEmailValid);

        boolean hasUniqueStrings = false;
        System.out.println("Имеются уникальные строки?: " + hasUniqueStrings);

        System.out.println("\n3. Аббревиатуры");

        String oldUuid = "123e4567-e89b-12d3-a456-426614174000";
        System.out.println("Старый universally unique identifier: " + oldUuid);

        String ramManufacturer = "Corsair";
        System.out.println("Производитель оперативной памяти: " + ramManufacturer);

        int hardDriveCapacity = 512;
        System.out.println("Емкость жесткого диска: " + hardDriveCapacity + " ГБ");

        String httpProtocol = "HTTP";
        System.out.println("Протокол передачи гипертекста: " + httpProtocol);

        String shortUrl = "vk.com/abc123";
        System.out.println("Сокращенный URL: " + shortUrl);

        String newClientId = "ID12345";
        System.out.println("Новый идентификатор клиента: " + newClientId);

        String asciiStandard = "ASCII";
        System.out.println("American standard code for information interchange: " + asciiStandard);

    }
}
